class TestLocalConfigMerge:
    """Test that local.yaml permissions are deep-merged during sync."""

    def test_local_permissions_appear_in_synced_output(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        # Create a local.yaml with extra SSH permission
        local_yaml = project / ".agent" / "local.yaml"
        local_yaml.write_text(
            "permissions:\n"
            "  allow:\n"
            "    shell:\n"
            "      remote:\n"
            "        - \"ssh -i ~/.ssh/my_key user@myhost *\"\n"
        )
        runner = CliRunner()
        result = runner.invoke(cli, ["sync", str(project), "-t", "claude"])
        assert result.exit_code == 0

        # The SSH command from local.yaml should appear in Claude settings
        settings_path = project / ".claude" / "settings.local.json"
        settings = json.loads(settings_path.read_text())
        allow = settings["permissions"]["allow"]
        assert any("ssh" in p and "my_key" in p for p in allow)

    def test_sync_works_without_local_yaml(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        # Ensure no local.yaml exists
        local_path = project / ".agent" / "local.yaml"
        if local_path.exists():
            local_path.unlink()
        runner = CliRunner()
        result = runner.invoke(cli, ["sync", str(project), "-t", "claude"])
        assert result.exit_code == 0
        assert (project / "CLAUDE.md").exists()

    def test_local_deny_extends_base_deny(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        local_yaml = project / ".agent" / "local.yaml"
        local_yaml.write_text(
            "permissions:\n"
            "  deny:\n"
            "    shell:\n"
            "      - \"sudo *\"\n"
        )
        runner = CliRunner()
        result = runner.invoke(cli, ["sync", str(project), "-t", "claude"])
        assert result.exit_code == 0

        settings_path = project / ".claude" / "settings.local.json"
        settings = json.loads(settings_path.read_text())
        deny = settings["permissions"]["deny"]
        # Original deny entries should still be present
        assert "Bash(rm -rf *)" in deny
        # New local deny should also be present
        assert "Bash(sudo *)" in deny
