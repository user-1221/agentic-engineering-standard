class TestSkillsAsSeparateFiles:
    """Test that skills are synced as separate .claude/commands/skills/*.md files."""

    def test_claude_skills_as_separate_commands(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()
        result = runner.invoke(cli, ["sync", str(project), "-t", "claude"])
        assert result.exit_code == 0

        # Each skill should have its own file under .claude/commands/skills/
        skills_dir = project / ".claude" / "commands" / "skills"
        assert skills_dir.exists()
        assert (skills_dir / "discover.md").exists()
        assert (skills_dir / "examine.md").exists()
        assert (skills_dir / "train.md").exists()

        # Skill files should have sentinel and runbook content
        discover_content = (skills_dir / "discover.md").read_text()
        assert "Generated by `aes sync`" in discover_content

    def test_claude_md_has_skill_index(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()
        runner.invoke(cli, ["sync", str(project), "-t", "claude"])

        content = (project / "CLAUDE.md").read_text()
        # Should have skill index with slash command references
        assert "/skills/discover" in content
        assert "/skills/examine" in content
        assert "/skills/train" in content
        assert "Available Skills" in content

    def test_claude_md_no_inlined_runbooks(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()
        runner.invoke(cli, ["sync", str(project), "-t", "claude"])

        content = (project / "CLAUDE.md").read_text()
        # Should NOT have the full "Skills Reference" section with inlined runbooks
        assert "Skills Reference" not in content

    def test_skill_command_no_collision(self, tmp_path: Path) -> None:
        """ml-pipeline has both command 'train' and skill 'train' — no collision."""
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()
        result = runner.invoke(cli, ["sync", str(project), "-t", "claude"])
        assert result.exit_code == 0

        # Command at .claude/commands/train.md
        cmd_path = project / ".claude" / "commands" / "train.md"
        assert cmd_path.exists()

        # Skill at .claude/commands/skills/train.md (no collision)
        skill_path = project / ".claude" / "commands" / "skills" / "train.md"
        assert skill_path.exists()

        # They should have different content
        assert cmd_path.read_text() != skill_path.read_text()

    def test_cursor_still_inlines_skills(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()
        runner.invoke(cli, ["sync", str(project), "-t", "cursor"])

        content = (project / ".cursorrules").read_text()
        # Cursor should still have inlined skill runbooks
        assert "Skills Reference" in content

    def test_sync_clean_removes_skill_files(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()

        # Sync first
        runner.invoke(cli, ["sync", str(project), "-t", "claude"])
        skills_dir = project / ".claude" / "commands" / "skills"
        assert (skills_dir / "discover.md").exists()

        # Clean should remove them
        result = runner.invoke(cli, ["sync", str(project), "--clean"])
        assert result.exit_code == 0
        assert not (skills_dir / "discover.md").exists()

    def test_sync_manifest_tracks_skill_files(self, tmp_path: Path) -> None:
        project = _copy_example("ml-pipeline", tmp_path)
        runner = CliRunner()
        runner.invoke(cli, ["sync", str(project), "-t", "claude"])

        manifest = json.loads((project / ".aes-sync.json").read_text())
        assert ".claude/commands/skills/discover.md" in manifest["files"]
        assert ".claude/commands/skills/examine.md" in manifest["files"]
        assert ".claude/commands/skills/train.md" in manifest["files"]


