# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _make_ctx(
    tmp_path: Path,
    manifest: Optional[dict] = None,
    permissions: Optional[dict] = None,
    skill_runbooks: Optional[Dict[str, str]] = None,
    skill_metadata: Optional[Dict[str, Dict[str, Any]]] = None,
    instructions: str = "Test instructions.",
    orchestrator: Optional[str] = None,
    memory_project: Optional[str] = None,
) -> AgentContext:
    """Build a minimal AgentContext for testing."""
    agent_dir = tmp_path / ".agent"
    agent_dir.mkdir(exist_ok=True)
    return AgentContext(
        project_root=tmp_path,
        agent_dir=agent_dir,
        manifest=manifest or {"name": "test-project", "version": "1.0.0"},
        instructions=instructions,
        orchestrator=orchestrator,
        skill_runbooks=skill_runbooks or {},
        permissions=permissions,
        commands=[],
        memory_project=memory_project,
        skill_metadata=skill_metadata or {},
    )


